const crypto = require('node:crypto');
const fs = require('node:fs');
const path = require('node:path');

const { repoRoot } = require('./lib/runtime');

const outputPath = path.join(
  repoRoot,
  'docs',
  'strategy',
  'context',
  'registry',
  '_index.generated.md',
);
const checkMode = process.argv[2] === '--check';

function listFilesRecursive(root, predicate) {
  const output = [];

  function walk(current) {
    if (!fs.existsSync(current)) {
      return;
    }

    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const fullPath = path.join(current, entry.name);
      if (entry.isDirectory()) {
        walk(fullPath);
      } else if (predicate(fullPath, entry.name)) {
        output.push(fullPath);
      }
    }
  }

  walk(root);
  return output.sort((a, b) => a.localeCompare(b));
}

function listImmediate(root, predicate) {
  if (!fs.existsSync(root)) {
    return [];
  }

  return fs
    .readdirSync(root, { withFileTypes: true })
    .filter((entry) => predicate(entry))
    .map((entry) => path.join(root, entry.name))
    .sort((a, b) => a.localeCompare(b));
}

function bulletize(items) {
  if (items.length === 0) {
    return ['- `(none)`', ''];
  }
  return [...items.map((item) => `- \`${item}\``), ''];
}

function section(title, lines) {
  return [`## ${title}`, '', ...lines];
}

function routePathFromPage(filePath) {
  const relative = path
    .relative(path.join(repoRoot, 'app'), path.dirname(filePath))
    .replace(/\\/g, '/');
  return relative ? `/${relative}` : '/';
}

function countTopLevelTsFiles(dirPath) {
  if (!fs.existsSync(dirPath)) {
    return 0;
  }

  return fs
    .readdirSync(dirPath, { withFileTypes: true })
    .filter((entry) => entry.isFile() && /\.(ts|tsx)$/.test(entry.name)).length;
}

function renderContent() {
  const routes = listFilesRecursive(
    path.join(repoRoot, 'app'),
    (fullPath, name) => name === 'page.tsx',
  ).map(routePathFromPage);
  const hooks = listImmediate(
    path.join(repoRoot, 'hooks'),
    (entry) => entry.isFile() && /^use.*\.(ts|tsx)$/.test(entry.name),
  ).map((filePath) => path.basename(filePath));

  const libDirRoot = path.join(repoRoot, 'lib');
  const libDirectories = listImmediate(libDirRoot, (entry) => entry.isDirectory()).map(
    (dirPath) => {
      const name = path.basename(dirPath);
      return `- \`${name}/\` (${countTopLevelTsFiles(dirPath)} files)`;
    },
  );
  const libFiles = listImmediate(
    libDirRoot,
    (entry) => entry.isFile() && /\.(ts|tsx)$/.test(entry.name),
  ).map((filePath) => path.basename(filePath));

  const governadaRoot = path.join(repoRoot, 'components', 'governada');
  const governadaDirectories = listImmediate(governadaRoot, (entry) => entry.isDirectory()).map(
    (dirPath) => {
      const name = path.basename(dirPath);
      return `- \`${name}/\` (${countTopLevelTsFiles(dirPath)} files)`;
    },
  );
  const governadaFiles = listImmediate(
    governadaRoot,
    (entry) => entry.isFile() && /\.(ts|tsx)$/.test(entry.name),
  ).map((filePath) => path.basename(filePath));

  const inngestFunctions = listFilesRecursive(
    path.join(repoRoot, 'inngest', 'functions'),
    (fullPath, name) => name.endsWith('.ts'),
  ).map((filePath) =>
    path.relative(path.join(repoRoot, 'inngest', 'functions'), filePath).replace(/\\/g, '/'),
  );

  const lines = [
    '# Structural Index (Auto-Generated)',
    '',
    '> Generated by `npm run registry:index`. Do NOT edit manually.',
    '> Used for staleness detection - agents should read `product-registry.md` instead.',
    '',
    ...section('Routes', bulletize(routes)),
    ...section('Hooks', bulletize(hooks)),
    ...section(
      'Lib Directories',
      libDirectories.length > 0 ? [...libDirectories, ''] : ['- `(none)`', ''],
    ),
    ...section('Lib Standalone Files', bulletize(libFiles)),
    '## Components (governada/)',
    '',
    '### Directories',
    '',
    ...(governadaDirectories.length > 0 ? [...governadaDirectories, ''] : ['- `(none)`', '']),
    '### Files',
    '',
    ...bulletize(governadaFiles),
    ...section('Inngest Functions', bulletize(inngestFunctions)),
  ];

  const body = `${lines.join('\n')}\n`;
  const checksum = crypto.createHash('sha256').update(body).digest('hex');
  const generatedAt = new Date().toISOString().replace(/\.\d{3}Z$/, 'Z');

  return `${body}---\n\nChecksum: \`${checksum}\`\nGenerated: ${generatedAt}\n`;
}

function stripFooter(content) {
  const lines = content.split(/\r?\n/);
  return `${lines.slice(0, -3).join('\n')}\n`;
}

function describeFirstDifference(oldText, newText) {
  const oldLines = oldText.split(/\r?\n/);
  const newLines = newText.split(/\r?\n/);
  const max = Math.max(oldLines.length, newLines.length);

  for (let index = 0; index < max; index += 1) {
    if ((oldLines[index] || '') !== (newLines[index] || '')) {
      const lineNumber = index + 1;
      return [
        `First difference at line ${lineNumber}:`,
        `  old: ${oldLines[index] || '(missing)'}`,
        `  new: ${newLines[index] || '(missing)'}`,
      ].join('\n');
    }
  }

  return 'Contents differ.';
}

function main() {
  const rendered = renderContent();

  if (checkMode) {
    if (!fs.existsSync(outputPath)) {
      console.error('WARNING: Registry index does not exist. Run: npm run registry:index');
      process.exit(1);
    }

    const current = fs.readFileSync(outputPath, 'utf8');
    if (stripFooter(current) !== stripFooter(rendered)) {
      console.error('WARNING: Registry index is stale. Run: npm run registry:index');
      console.error(describeFirstDifference(stripFooter(current), stripFooter(rendered)));
      process.exit(1);
    }

    process.exit(0);
  }

  fs.writeFileSync(outputPath, rendered);
  const entryCount = rendered.split(/\r?\n/).filter((line) => line.startsWith('- ')).length;
  console.log(`Registry index generated: ${outputPath} (${entryCount} entries)`);
}

if (require.main === module) {
  main();
}

module.exports = {
  outputPath,
  renderContent,
  stripFooter,
};
