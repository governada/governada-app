import { createHash } from 'node:crypto';
import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';

import { getScriptContext } from './lib/runtime.mjs';

function listFiles(dir) {
  if (!existsSync(dir)) {
    return [];
  }

  return readdirSync(dir, { withFileTypes: true });
}

function formatListItem(value) {
  return `- \`${value}\``;
}

function collectRoutes(root) {
  const routes = [];
  const appDir = path.join(root, 'app');

  const stack = [appDir];
  while (stack.length > 0) {
    const current = stack.pop();
    for (const entry of listFiles(current)) {
      const fullPath = path.join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(fullPath);
        continue;
      }

      if (entry.isFile() && entry.name === 'page.tsx') {
        routes.push(
          fullPath
            .replace(/\\/g, '/')
            .replace(/\/page\.tsx$/, '')
            .replace(/^.*\/app/, ''),
        );
      }
    }
  }

  return routes.sort();
}

function collectHookFiles(root) {
  const hooksDir = path.join(root, 'hooks');
  if (!existsSync(hooksDir)) {
    return [];
  }

  return readdirSync(hooksDir)
    .filter((name) => /^use.*\.(ts|tsx)$/u.test(name))
    .sort();
}

function collectLibDirectories(root) {
  const libDir = path.join(root, 'lib');
  if (!existsSync(libDir)) {
    return [];
  }

  return readdirSync(libDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .sort((a, b) => a.name.localeCompare(b.name))
    .map((entry) => {
      const dirPath = path.join(libDir, entry.name);
      const files = readdirSync(dirPath).filter((name) => /\.(ts|tsx)$/u.test(name));
      return { name: entry.name, count: files.length };
    });
}

function collectLibStandaloneFiles(root) {
  const libDir = path.join(root, 'lib');
  if (!existsSync(libDir)) {
    return [];
  }

  return readdirSync(libDir)
    .filter((name) => /\.(ts|tsx)$/u.test(name))
    .sort();
}

function collectGovernadaComponentDirectories(root) {
  const dir = path.join(root, 'components', 'governada');
  if (!existsSync(dir)) {
    return [];
  }

  return readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .sort((a, b) => a.name.localeCompare(b.name))
    .map((entry) => {
      const fullPath = path.join(dir, entry.name);
      const files = readdirSync(fullPath).filter((name) => /\.(ts|tsx)$/u.test(name));
      return { name: entry.name, count: files.length };
    });
}

function collectGovernadaComponentFiles(root) {
  const dir = path.join(root, 'components', 'governada');
  if (!existsSync(dir)) {
    return [];
  }

  return readdirSync(dir)
    .filter((name) => /\.tsx$/u.test(name))
    .sort();
}

function collectInngestFunctions(root) {
  const dir = path.join(root, 'inngest', 'functions');
  if (!existsSync(dir)) {
    return [];
  }

  const stack = [dir];
  const files = [];
  while (stack.length > 0) {
    const current = stack.pop();
    for (const entry of listFiles(current)) {
      const fullPath = path.join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(fullPath);
      } else if (entry.isFile() && /\.ts$/u.test(entry.name)) {
        files.push(fullPath.replace(/\\/g, '/').replace(/^.*\/inngest\/functions\//, ''));
      }
    }
  }

  return files.sort();
}

function buildBody(root) {
  const lines = [
    '# Structural Index (Auto-Generated)',
    '',
    '> Generated by `node scripts/generate-registry-index.mjs`. Do NOT edit manually.',
    '> Used for staleness detection - agents should read `product-registry.md` instead.',
    '',
  ];

  lines.push('## Routes', '');
  for (const route of collectRoutes(root)) {
    lines.push(formatListItem(route));
  }
  lines.push('');

  lines.push('## Hooks', '');
  for (const hook of collectHookFiles(root)) {
    lines.push(formatListItem(hook));
  }
  lines.push('');

  lines.push('## Lib Directories', '');
  for (const dir of collectLibDirectories(root)) {
    lines.push(`- \`${dir.name}/\` (${dir.count} files)`);
  }
  lines.push('');

  lines.push('## Lib Standalone Files', '');
  for (const file of collectLibStandaloneFiles(root)) {
    lines.push(formatListItem(file));
  }
  lines.push('');

  lines.push('## Components (governada/)', '', '### Directories', '');
  for (const dir of collectGovernadaComponentDirectories(root)) {
    lines.push(`- \`${dir.name}/\` (${dir.count} files)`);
  }
  lines.push('', '### Files', '');
  for (const file of collectGovernadaComponentFiles(root)) {
    lines.push(formatListItem(file));
  }
  lines.push('');

  lines.push('## Inngest Functions', '');
  for (const file of collectInngestFunctions(root)) {
    lines.push(formatListItem(file));
  }
  lines.push('');

  return lines.join('\n');
}

function checksum(text) {
  return createHash('sha256').update(text, 'utf8').digest('hex');
}

const { repoRoot: root } = getScriptContext(import.meta.url);
const outPath = path.join(root, 'docs', 'strategy', 'context', 'registry', '_index.generated.md');
const body = buildBody(root);
const digest = checksum(body);
const generated = `${body}---\nChecksum: \`${digest}\`\nGenerated: ${new Date().toISOString().replace('T', ' ').slice(0, 19)}Z\n`;

if (process.argv[2] === '--check') {
  if (!existsSync(outPath)) {
    console.log(
      'WARNING: Registry index does not exist. Run: node scripts/generate-registry-index.mjs',
    );
    process.exit(1);
  }

  const existing = readFileSync(outPath, 'utf8');
  const existingBody = existing.split('\n').slice(0, -3).join('\n').trimEnd();
  const newBody = generated.split('\n').slice(0, -3).join('\n').trimEnd();

  if (existingBody !== newBody) {
    console.log('WARNING: Registry index is stale. Run: node scripts/generate-registry-index.mjs');
    process.exit(1);
  }

  process.exit(0);
}

mkdirSync(path.dirname(outPath), { recursive: true });
writeFileSync(outPath, generated, 'utf8');
console.log(
  `Registry index generated: ${path.relative(root, outPath)} (${body.split('\n').filter((line) => line.startsWith('- ')).length} entries)`,
);
